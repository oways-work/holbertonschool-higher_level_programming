#!/usr/bin/python3
"""
This module provides a function to compute a to the power of b.
"""


def pow(a, b):
    """
    Computes a to the power of b and returns the value.

    Args:
        a (int): The base number.
        b (int): The exponent.

    Returns:
        int/float: The result of a ^ b.
    """
    return (a ** b)
